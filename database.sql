CREATE DATABASE stu_results;

USE stu_results;

CREATE TABLE main_table(  
    reg VARCHAR(8) NOT NULL PRIMARY KEY,
    password VARCHAR(120) NOT NULL,  
    name VARCHAR(30) DEFAULT NULL,
    father_name VARCHAR(30) DEFAULT NULL,
    DOB date DEFAULT NULL,
    current_sem VARCHAR(4) DEFAULT '9', 
    mail VARCHAR(50) DEFAULT NULL,
    phone VARCHAR(10) DEFAULT NULL,
    nationality VARCHAR(30) DEFAULT NULL,
    category VARCHAR(10) DEFAULT NULL,
    religion VARCHAR(20) DEFAULT NULL,
    caste VARCHAR(20) DEFAULT NULL,
    blood_group VARCHAR(3) DEFAULT NULL,
    branch VARCHAR(20) DEFAULT NULL
);

CREATE TABLE marks_table(   
    reg VARCHAR(8) NOT NULL,
    sem INT(11) DEFAULT NULL,
    subject_code VARCHAR(10) DEFAULT NULL,
    grade INT(11) DEFAULT NULL,
    credits INT(11) DEFAULT NULL
);

CREATE TABLE admin_table(
    reg VARCHAR(8) PRIMARY KEY
)

--The password stored is generated by generate_password_hash function of python werkzeug.security .